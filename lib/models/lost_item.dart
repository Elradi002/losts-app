class LostItem {
  String id;
  final String name;
  final DateTime date;
  final String type;
  final String place;
  final int phoneNumber;
  final String description;
  LostItem({
    required this.id,
    required this.name,
    required this.date,
    required this.type,
    required this.place,
    required this.phoneNumber,
    required this.description,
  });
}
